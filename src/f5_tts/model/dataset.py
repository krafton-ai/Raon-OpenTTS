# ── Audio utilities ───────────────────────────────────────────────────────────

def normalize_peak(audio, eps=1e-3):
    max_abs = np.max(np.abs(audio))
    max_abs = max(max_abs, eps)
    audio = audio / max_abs
    return np.clip(audio, -1.0, 1.0)


def decode_opus_bytes(opus_bytes: bytes) -> np.ndarray:
    """Decode 16 kHz mono Opus bytes → peak-normalised float32 numpy array."""
    audio, sr = sf.read(io.BytesIO(opus_bytes), dtype="float32")
    assert sr == 16000, f"Expected 16 kHz audio, got {sr} Hz"
    if audio.ndim > 1:
        audio = audio.mean(axis=1)
    return normalize_peak(audio)


# ── Tar index (low-level, no tarfile object in memory) ───────────────────────

def _build_tar_index(tar_path: str) -> dict:
    """
    Build {member_name: (data_offset, data_size)} by reading tar headers.
    Saves result to {tar_path}.idx (pickle) for fast reuse.
    """
    idx_path = tar_path + ".idx"
    if os.path.exists(idx_path):
        with open(idx_path, "rb") as f:
            # Index files are self-generated by this function; not user-supplied
            return pickle.load(f)  # noqa: S301

    index = {}
    with open(tar_path, "rb") as f:
        while True:
            header = f.read(512)
            if len(header) < 512 or header == b"\x00" * 512:
                break
            name_raw = header[:100].rstrip(b"\x00")
            if not name_raw:
                break
            name = name_raw.decode("utf-8", errors="replace")
            # Typeflag: '0' or '\0' = regular file, 'L' = long name (GNU tar)
            typeflag = chr(header[156])
            size_raw = header[124:136].strip().rstrip(b"\x00")
            size = int(size_raw, 8) if size_raw else 0
            data_offset = f.tell()
            if typeflag in ("0", "\x00"):
                index[name] = (data_offset, size)
            # Advance to next 512-byte aligned block
            f.seek(((size + 511) // 512) * 512, os.SEEK_CUR)

    try:
        with open(idx_path, "wb") as f:
            pickle.dump(index, f, protocol=4)
    except Exception:
        pass  # read-only filesystem; index stays in memory only
    return index


# Per-process tar index cache (reset after fork via __getstate__)
_tar_index_cache: dict = {}
_tar_index_lock = threading.Lock()
_MAX_CACHED_INDICES = 16


def _get_tar_index(tar_path: str) -> dict:
    with _tar_index_lock:
        if tar_path not in _tar_index_cache:
            if len(_tar_index_cache) >= _MAX_CACHED_INDICES:
                oldest = next(iter(_tar_index_cache))
                del _tar_index_cache[oldest]
            _tar_index_cache[tar_path] = _build_tar_index(tar_path)
        return _tar_index_cache[tar_path]


def _read_tar_member(tar_path: str, name: str) -> bytes:
    index = _get_tar_index(tar_path)
    if name not in index:
        raise KeyError(f"{name!r} not found in {tar_path}")
    offset, size = index[name]
    with open(tar_path, "rb") as f:
        f.seek(offset)
        return f.read(size)


# ── TarDataset ────────────────────────────────────────────────────────────────

class TarDataset(Dataset):
    """
    Loads audio from RAON-TTS-Pool WebDataset tar shards.

    Each record has: sample_key, text, duration, shard_name, tar_path.
    Audio is decoded lazily from the tar on __getitem__.
    The tar index cache is per-process (safe with DataLoader fork).
    """

    MAX_AUDIO_DUR = float(os.environ.get("MAX_AUDIO_DUR", "30.0"))

    def __init__(
        self,
        records: list,
        target_sample_rate: int = 16_000,
        hop_length: int = 256,
    ):
        self.records = records
        self.durations = [r["duration"] for r in records]
        self.target_sample_rate = target_sample_rate
        self.hop_length = hop_length

    # ── pickle / fork safety ──────────────────────────────────────────────────
    # DataLoader with fork creates child processes that share this object.
    # _tar_index_cache is module-level, so each forked worker has its own copy.
    # No extra work needed here; we just avoid storing any file handles as attrs.

    def get_frame_len(self, idx: int) -> float:
        dur = min(self.durations[idx], self.MAX_AUDIO_DUR)
        return dur * self.target_sample_rate / self.hop_length

    def __len__(self) -> int:
        return len(self.records)

    def __getitem__(self, idx: int):
        n = len(self.records)
        tries = 0
        while tries < 10:
            rec = self.records[idx]
            try:
                opus_bytes = _read_tar_member(
                    rec["tar_path"], f"{rec['sample_key']}.opus"
                )
                audio = decode_opus_bytes(opus_bytes)
                duration = len(audio) / self.target_sample_rate

                if duration > self.MAX_AUDIO_DUR:
                    audio = audio[: int(self.MAX_AUDIO_DUR * self.target_sample_rate)]
                    duration = len(audio) / self.target_sample_rate

                return {
                    "audio": torch.from_numpy(audio).unsqueeze(0),
                    "text": rec["text"],
                    "duration": duration,
                    "audio_path": rec["sample_key"],
                }
            except Exception as e:
                logger.debug(f"TarDataset skip idx={idx}: {e}")
                idx = (idx + 1) % n
                tries += 1
        return None


# ── load_raon_pool ────────────────────────────────────────────────────────────

def load_raon_pool(
    hf_repo: str = "KRAFTON/RAON-TTS-Pool",
    local_cache_dir: str = None,
    configs: list = None,
    split: str = "core",
    nonredist_dirs: list = None,
    target_sample_rate: int = 16_000,
    hop_length: int = 256,
    **_,
):
    """
    Load RAON-TTS-Pool from HuggingFace Hub (or local cache) and return
    a TarDataset + durations list suitable for DynamicBatchSampler.

    Args:
        hf_repo:         HuggingFace dataset repo id.
        local_cache_dir: Local path to already-downloaded Hub snapshot.
                         If None, calls snapshot_download() to fetch it.
        configs:         Sub-dataset subdirectory names to include, e.g.
                         ["LibriHeavy", "HiFiTTS"].  None = all with parquets.
        split:           "core" (quality-filtered) or "pool" (all samples).
        nonredist_dirs:  List of local directories prepared with
                         prepare_nonredist_datasets.py (GigaSpeech, SPGISpeech2).
                         Each must contain metadata_{split}.parquet + tar files.

    Returns:
        (TarDataset, list[float])  – dataset and per-sample durations.
    """
    # ── 1. Resolve local root ─────────────────────────────────────────────────
    if local_cache_dir and os.path.isdir(local_cache_dir):
        hub_dir = local_cache_dir
        logger.info(f"Using local cache: {hub_dir}")
    else:
        from huggingface_hub import snapshot_download
        logger.info(f"Downloading {hf_repo} …")
        hub_dir = snapshot_download(
            hf_repo,
            repo_type="dataset",
            local_dir=local_cache_dir,
        )
        logger.info(f"Downloaded to: {hub_dir}")

    # ── 2. Collect tar directories ────────────────────────────────────────────
    all_dirs = []

    if configs:
        for cfg in configs:
            d = os.path.join(hub_dir, cfg)
            if os.path.isdir(d):
                all_dirs.append(d)
            else:
                logger.warning(f"Config dir not found: {d}")
    else:
        # Auto-detect: any subdir containing the target parquet
        for entry in sorted(os.listdir(hub_dir)):
            d = os.path.join(hub_dir, entry)
            if os.path.isdir(d) and os.path.exists(
                os.path.join(d, f"metadata_{split}.parquet")
            ):
                all_dirs.append(d)

    if nonredist_dirs:
        for d in nonredist_dirs:
            if os.path.isdir(d):
                all_dirs.append(d)
            else:
                logger.warning(f"nonredist_dir not found: {d}")

    if not all_dirs:
        raise ValueError(
            f"No dataset directories found under {hub_dir} with "
            f"metadata_{split}.parquet. "
            f"Run snapshot_download or check --local_cache_dir."
        )

    # ── 3. Load parquet metadata ──────────────────────────────────────────────
    all_records = []
    for tar_dir in all_dirs:
        parquet_path = os.path.join(tar_dir, f"metadata_{split}.parquet")
        if not os.path.exists(parquet_path):
            logger.warning(f"No {split} parquet in {tar_dir}, skipping")
            continue
        table = pq.read_table(
            parquet_path,
            columns=["sample_key", "text", "duration", "shard_name"],
        )
        for row in table.to_pylist():
            tar_path = os.path.join(tar_dir, row["shard_name"])
            if not os.path.exists(tar_path):
                continue
            row["tar_path"] = tar_path
            all_records.append(row)

    logger.info(
        f"Loaded {len(all_records):,} records from {len(all_dirs)} dirs "
        f"(split={split})"
    )

    dataset = TarDataset(
        all_records,
        target_sample_rate=target_sample_rate,
        hop_length=hop_length,
    )
    durations = [r["duration"] for r in all_records]
    return dataset, durations


# ── DynamicBatchSampler ───────────────────────────────────────────────────────

def save_batches(batches, path="dynamic_batches.json"):
    with open(path, "w") as f:
        json.dump(batches, f)
    logger.info(f"[Saved] {len(batches)} batches -> {path}")


def load_batches(path="dynamic_batches.json"):
    with open(path, "r") as f:
        batches = json.load(f)
    logger.info(f"[Loaded] {len(batches)} batches from {path}")
    return batches


class DynamicBatchSampler(Sampler[list[int]]):
    def __init__(
        self,
        sampler: Sampler[int],
        frames_threshold: int,
        max_samples: int = 0,
        random_seed=None,
        drop_residual: bool = False,
        save_path: str = "dynamic_batches.json",
        load_if_exists: bool = True,
        max_padded_frames: int = 0,
    ):
        self.sampler = sampler
        self.frames_threshold = frames_threshold
        self.max_samples = max_samples
        self.max_padded_frames = max_padded_frames
        self.random_seed = random_seed
        self.epoch = 0
        self.drop_last = True
        self.save_path = save_path

        if load_if_exists and os.path.exists(save_path):
            self.batches = load_batches(save_path)
            return

        indices, batches = [], []
        data_source = self.sampler.data_source

        for idx in tqdm(
            self.sampler,
            desc="Sorting with sampler... if slow, check whether dataset is provided with duration",
        ):
            indices.append((idx, data_source.get_frame_len(idx)))
        indices.sort(key=lambda elem: elem[1])

        batch = []
        batch_frames = 0
        batch_max_frame = 0
        for idx, frame_len in tqdm(
            indices,
            desc=f"Creating dynamic batches with {frames_threshold} audio frames per gpu",
        ):
            new_max_frame = max(batch_max_frame, frame_len)
            new_count = len(batch) + 1
            padded_ok = (max_padded_frames == 0) or (
                new_max_frame * new_count <= max_padded_frames
            )

            if (
                batch_frames + frame_len <= self.frames_threshold
                and (max_samples == 0 or len(batch) < max_samples)
                and padded_ok
            ):
                batch.append(idx)
                batch_frames += frame_len
                batch_max_frame = new_max_frame
            else:
                if len(batch) > 0:
                    batches.append(batch)
                if frame_len <= self.frames_threshold:
                    batch = [idx]
                    batch_frames = frame_len
                    batch_max_frame = frame_len
                else:
                    batch = []
                    batch_frames = 0
                    batch_max_frame = 0

        if not drop_residual and len(batch) > 0:
            batches.append(batch)

        del indices
        self.batches = batches
        save_batches(batches, save_path)

    def set_epoch(self, epoch: int) -> None:
        self.epoch = epoch

    def __iter__(self):
        if self.random_seed is not None:
            g = torch.Generator()
            g.manual_seed(self.random_seed + self.epoch)
            indices = torch.randperm(len(self.batches), generator=g).tolist()
            batches = [self.batches[i] for i in indices]
        else:
            batches = self.batches
        return iter(batches)

    def __len__(self):
        return len(self.batches)


# ── collate_fn ────────────────────────────────────────────────────────────────

def collate_fn(batch):
    batch = [item for item in batch if item is not None]
    if len(batch) == 0:
        return None

    audio_list = [item["audio"].squeeze(0) for item in batch]
    audio_lengths = torch.LongTensor([audio.shape[-1] for audio in audio_list])

    max_audio_length = audio_lengths.amax()
    padded_audios = []
    for audio in audio_list:
        padding = (0, max_audio_length - audio.size(-1))
        padded_audios.append(F.pad(audio, padding, value=0))
    audios = torch.stack(padded_audios)

    hop_length = 256
    n_fft = 1024

    def compute_mel_length(lengths, n_fft=1024, hop_length=256):
        return 1 + (lengths - n_fft) // hop_length

    mel_lengths = compute_mel_length(audio_lengths, n_fft, hop_length).clamp(min=1)

    return dict(
        audio=audios,
        audio_lengths=audio_lengths,
        mel_lengths=mel_lengths,
        text=[item["text"] for item in batch],
        durations=[item["duration"] for item in batch],
        audio_paths=[item["audio_path"] for item in batch],
    )
